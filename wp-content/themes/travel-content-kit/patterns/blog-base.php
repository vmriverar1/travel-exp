<p class="has-satoshi-font-family">TPeruRail is the only train service that departs from the San Pedro Train station. These trains will cover the longest distance and are only available in 2 schedules. Trains from this station run from April to late December.<br><br>This coincides with the dry season in the Andes.<br>San Pedro Train Station is located in the center of Cusco near the market of the same name. It is a short walk from Plaza de Armas. After leaving Cusco along a series of switchbacks, to get out of the valley of Cusco. The train follows the tracks through the Sacred Valley, alongside the Urubamba River. It then continues to Aguas Calientes - The gateway to Machu Picchu.<br><br>These trains are recommended for those who enjoy train travel specifically. As well as the varied terrains of the Andean region. The journey to Aguas Calientes takes around five hours.</p>
